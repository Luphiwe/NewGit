public class Git {
    /**
     * this adds two integers
     * @param a
     * @param b
     * @return 
     */
     public static int intergerSum(int a, int b) {
        
        return a+b;
    }
     /** 
      * this adds two doubles
      * @param c
      * @param d
      * @return 
      */
     
     public static double doubleSum(int c, int d) {
         return c+d;
     }
}
